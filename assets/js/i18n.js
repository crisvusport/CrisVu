/* Ngôn ngữ hiện tại (lưu trong URL ?lang= để chia sẻ được, không dùng localStorage) */
function getLang(){
  const p = new URLSearchParams(location.search).get("lang");
  return (p === "en") ? "en" : "vi";
}
function t(key){
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key]) || (I18N.vi[key]) || key;
}

/* Ghép tên sản phẩm từ các tag, theo đúng công thức của shop */
function productName(p){
  const lang = getLang();
  const parts = [];
  parts.push(t("loai." + p.loai));
  parts.push(p.team);
  if(p.season) parts.push(p.season);
  parts.push(t("kit." + p.kit));
  if(p.version) parts.push(t("version." + p.version));
  (p.special || []).forEach(s => parts.push(t("special." + s)));
  return parts.join(" · ");
}

/* Áp dụng dịch cho mọi phần tử có data-i18n */
function applyI18n(root){
  const scope = root || document;
  scope.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  scope.querySelectorAll("[data-i18n-ph]").forEach(el => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-ph")));
  });
  document.documentElement.lang = getLang();
}

/* Nút chuyển ngôn ngữ: đổi ?lang= và giữ nguyên các tham số khác */
function toggleLangUrl(){
  const params = new URLSearchParams(location.search);
  params.set("lang", getLang() === "vi" ? "en" : "vi");
  return location.pathname + "?" + params.toString();
}
